package com.hilti.utils;

/**
 * An enum listing supported field types.
 * Currently available values are: BUTTON, CHECKBOX, DIV, GROUP, IMAGE, LINK, LIST, RADIO, TEXT and SECTION.
 * @author Mateusz Mazurkiewicz
 * @see Field
 */
public enum FieldType {
    
	BUTTON,
	CHECKBOX,
	DIV,
	GROUP,
	IMAGE,
	LINK,
	LIST,
	RADIO,
	TEXT,
	SECTION;
	
	FieldType(){}
}
